// ─── Compute totals ────────────────────────────────────────────────────────────

function computeStoreTotals(
  items: GroceryItem[],
  deliveryFeeEur: number,
): {
  subtotalEur: number;
  taxEur: number;
  totalEur: number;
  availableItemCount: number;
  unavailableItemCount: number;
} {
  const subtotalEur = Math.round(items.reduce((sum, item) => sum + item.priceEur, 0) * 100) / 100;
  const taxEur = Math.round(subtotalEur * 0.08 * 100) / 100;
  const totalEur = Math.round((subtotalEur + taxEur + deliveryFeeEur) * 100) / 100;
  const availableItemCount = items.filter((i) => i.availabilityStatus !== 'OUT_OF_STOCK').length;
  const unavailableItemCount = items.filter((i) => i.availabilityStatus === 'OUT_OF_STOCK').length;
  return { subtotalEur, taxEur, totalEur, availableItemCount, unavailableItemCount };
}

